

string = 'ricardo is a very hansome boy'

print(string.find('hansome'))  # 查找索引
print(string.upper())  # 转大写
print(string.lower())  # 转小写
print(string.isdigit())  # 字符串是否只包含数字
print(string.replace('hansome', 'rich'))  # 替换
print(string.strip('y'))  # 去掉头尾的指定字符
print(string.split(' '))  # 切割
print(string.swapcase())  # 大小写互换
